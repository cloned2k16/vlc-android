#!/bin/sh

#-----------------------------------------------------------------------------------------------------------------------
    _ansiMsg        ()                                              {   
        local col;
        local who=${FUNCNAME[ 1 ]}
        case $who in
            _log)           col="\033[0;32m"                        ;;
            _inf)           col="\033[0;36m"                        ;;
            _wrn)           col="\033[0;33m"                        ;;
            _err)           col="\033[0;31m"                        ;;
            _abortError)    col="\033[38;2;255;11;33m"              ;;
            *)              col="\033[0m"                           ;;
        esac
        echo -e "$col $@ \033[0m"
    }
#-----------------------------------------------------------------------------------------------------------------------
    _log            ()                                              { _ansiMsg "~ $@" ; }
#-----------------------------------------------------------------------------------------------------------------------
    _inf            ()                                              { _ansiMsg "~ $@" ; }
#-----------------------------------------------------------------------------------------------------------------------
    _wrn            ()                                              { _ansiMsg "! $@" ; }
#-----------------------------------------------------------------------------------------------------------------------
    _err            ()                                              { _ansiMsg "! $@" ; }
#-----------------------------------------------------------------------------------------------------------------------
    _abortError     ()                                              {
            _ansiMsg "!! $@"
			cd "$SCRIPT_PATH"
            kill -s 2 $MYSELF
    }
#-----------------------------------------------------------------------------------------------------------------------
    _abortIfError   ()                                              {
        if [ ! $? -eq 0 ];then
            _abortError "$@"
        fi
    }
#-----------------------------------------------------------------------------------------------------------------------
